namespace DocordoAPI.Model.Domain
{
    using Newtonsoft.Json;

    public class DocordoProperty
    {
        [JsonProperty(PropertyName = "property_id")]
        public string PropertyId { get; set; }

        [JsonProperty(PropertyName = "name")]
        public string Name { get; set; }

        [JsonProperty(PropertyName = "type")]
        public int Type { get; set; }

        [JsonProperty(PropertyName = "length")]
        public int Length { get; set; }

        [JsonProperty(PropertyName = "decimal")]
        public int Decimal { get; set; }

        [JsonProperty(PropertyName = "is_multi_value")]
        public bool IsMultiValue { get; set; }

        [JsonProperty(PropertyName = "semantics")]
        public int Semantics { get; set; }

        [JsonProperty(PropertyName = "is_group_only")]
        public bool IsGroupOnly { get; set; }

        [JsonProperty(PropertyName = "is_non_group_only")]
        public bool IsNonGroupOnly { get; set; }

        [JsonProperty(PropertyName = "must_be_member_id")]
        public string MustBeMemberId { get; set; }

        [JsonProperty(PropertyName = "must_be_member_name")]
        public string MustBeMemberName { get; set; }

        [JsonProperty(PropertyName = "min_int")]
        public int MinInt { get; set; }

        [JsonProperty(PropertyName = "max_int")]
        public long MaxInt { get; set; }

        [JsonProperty(PropertyName = "min_date")]
        public string MinDate { get; set; }

        [JsonProperty(PropertyName = "max_date")]
        public string MaxDate { get; set; }

        [JsonProperty(PropertyName = "min_decimal")]
        public object MinDecimal { get; set; }

        [JsonProperty(PropertyName = "max_decimal")]
        public object MaxDecimal { get; set; }

        [JsonProperty(PropertyName = "regular_expression")]
        public string RegularExpression { get; set; }

        [JsonProperty(PropertyName = "using_lookup_set")]
        public bool UsingLookupSet { get; set; }

        [JsonProperty(PropertyName = "allow_non_matching_values")]
        public bool AllowNonMatchingValues { get; set; }

        [JsonProperty(PropertyName = "lookup_set_id")]
        public string LookupSetId { get; set; }

        [JsonProperty(PropertyName = "lookup_set_name")]
        public string LookupSetName { get; set; }
    }
}
